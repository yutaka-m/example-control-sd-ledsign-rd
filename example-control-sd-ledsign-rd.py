import serial
import struct

ser = serial.Serial(
    port = '/dev/cu.usbserial-1420',
    baudrate=12000000, 
    parity=serial.PARITY_NONE
    )

# LED Display SD-LEDSIGN-RD
# https://www.amazon.co.jp/gp/product/B01AUPJXYI/ref=ppx_yo_dt_b_asin_title_o02_s00?ie=UTF8&psc=1

# 1: order number
# end: checksum
data1 = [0x01, 0x05, 0x01, 0x55, 0xAA, 0x5A, 0xA5, 0x05]

# 1: order number
# 3: speed & direction
# end: checksum
data2 = [0x02, 0x08, 0x31, 0x91, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0xcc]

# 1: order number
# 4: display character length
# end: checksum
data3 = [0x03, 0x10, 0x00, 0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x14]

# 1: order number
# 2: led data length + 1
# 3: packet data counter.
# 4 ~ led data length: led image data per 8bits
#   (example
#     data: 0xA5
#     led pattern: o x o x x o x o
#   
# end: checksum
data4 = [0x04, 0x11, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x15]

# 1: order number
# end: checksum
data5 = [0x05, 0x03, 0x10, 0x03, 0x00, 0x1b]

ser.write(data1)
ser.write(data2)
ser.write(data3)
ser.write(data4)
ser.write(data5)

ser.close()
